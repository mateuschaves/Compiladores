
const ips = [
    '127.0.0.1',
    '1.0',
    '192.168.25.1',
    'oi',
    'rs',
    '10.0.0.1',
    '10.0.0.102:8080',
    '545454554545',
    '255.255.255.255',
    '256.255.0.1'
];

ips.map( ip => {
    if(/^((25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)/.exec(ip))
        console.log(`${ip} é um endereço de ip válido !\n`);
    else
        console.log(`${ip} não é um endereço de ip válido !\n`);
});